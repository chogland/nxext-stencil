export const getIconIndex = (nm: string, icons: any[]) => {
  let counter = 0;
  let foundAt = -1;

  for (let element of icons) {
    if (element.name === nm) {
      foundAt = counter;
      break;
    }
    counter++;
  }

  return foundAt;
};

